	var popDown = document.querySelector('.pop-down');
	
	document.querySelector('.pop-btn').addEventListener('click', function(e){
		e.preventDefault(); 
	
	
		if (popDown.getAttribute('data-state') == 'expanded'){
			
			popDown.setAttribute('data-state', 'collapsed');
		
		}else{
		
			popDown.setAttribute('data-state', 'expanded');
		
		}
	});
	
